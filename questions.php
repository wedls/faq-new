/**
 * Функция для получения вариантов ответов по модулю
 */
function getAnswerOptions($module, $questionNumber = null) {
    switch($module) {
        case 1:
            return [
                'true' => 'Верно',
                'false' => 'Неверно'
            ];
        case 2:
            // Для вопросов 61-95 (часть 2) другие варианты
            if ($questionNumber >= 61 && $questionNumber <= 95) {
                return [
                    1 => 'Согласен',
                    2 => 'Скорее согласен',
                    3 => 'Иногда',
                    4 => 'Скорее не согласен',
                    5 => 'Не согласен'
                ];
            } else {
                return [
                    0 => '0 - Никогда',
                    1 => '1 - Редко',
                    2 => '2 - Иногда',
                    3 => '3 - Часто',
                    4 => '4 - Постоянно'
                ];
            }
        case 3:
            return [
                1 => '1 - Категорически не согласен',
                2 => '2 - Скорее не согласен',
                3 => '3 - Затрудняюсь ответить',
                4 => '4 - Скорее согласен',
                5 => '5 - Полностью согласен'
            ];
        case 4:
            return [
                1 => '1 - Согласен',
                0 => '0 - Не согласен'
            ];
        default:
            return [];
    }
}
